/* primer ejercicio contador de 1 hasta 10 muestre cada numero*/
let contador = 1;
while (contador <= 10) {
  console.log(contador);
  contador++;
}

/* segundo ejercicio contador de 10 hasta 0 muestre cada numero*/

let descendente = 10;
while (descendente >= 0) {
  console.log(descendente);
  descendente--;
}

/* tercer ejercicio Crea un programa de cuenta progresiva. Pide un número y cuenta desde 0 hasta ese número utilizando un bucle 'while' en la consola del navegador.*/

let numero = prompt("Ingrese un número");

while (numero >= 0) {
  console.log(numero);
  numero--;
}

/* cuarto ejercicio  pide un numero cuenta desde 0 hasta ese numero*/

